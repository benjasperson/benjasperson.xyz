		<h2>Quote</h2>
		<p>Oh say, what is truth? 'Tis the fairest gem<br>
		That the riches of worlds can produce,<br>
		And priceless the value of truth will be when<br>
		The proud monarch's costliest diadem<br>
		Is counted but dross and refuse.</p>
		<p>Yes, say, what is truth? 'Tis the brightest prize<br>
		To which mortals or Gods can aspire.<br>
		Go search in the depths where it glittering lies,<br>
		Or ascend in pursuit to the loftiest skies:<br>
		'Tis an aim for the noblest desire.</p>
		<p><cite>Oh Say What is Truth</cite> (verses 1 and 2)<br>
		John Jaques, 1827 to 1900</p>
